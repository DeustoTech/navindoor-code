                                            'String','Max dB',          ...
                                            'Units','normalized',       ...
                                            'Position',[0.0825 0.3 0.3 0.4]);

                   edit_max_att = uicontrol('style','edit',               ...
                                            'Parent',panel_filter,        ...
                                            'String',' ',                 ...
                                            'Units','normalized',         ...
                                            'Position',[0.4 0.325 0.15 0.3], ...
                                            'Tag','Max dB');               

                   btn_filter  = uicontrol('style','pushbutton',          ...
                                           'Parent',panel_filter,         ...
                                           'Units','normalized',          ...
                                           'String','Filter!',            ...
                                           'Position',[0.7 0.325 0.2 0.3]);
    %% Beacon Free
   tab_beacon_free_sgn    = uitab(tabgp,'Title','Beacon Free','Tag','Beacon Free');
   tab_beacon_free_sgn.ForegroundColor  = color;

           popBBS = uicontrol('style','popupmenu',  ...
