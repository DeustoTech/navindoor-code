                              'Units','normalized', ...
                              'Position',[0.1 0.3 0.8 0.2], ...
                              'Parent',tab_beacon_free_sgn, ...
                              'Tag','popupmenu');
   


   
    %%  Engine Functions 
    uipanel(panel_control,'Units','normalize',                ...
                       'Position',[0.4 0.02 0.475 0.96],      ...
                       'Title','Engine functions',            ...
                       'Tag','SignalType'); 
           
    %% Generation Button       
    generation_panel = uipanel(panel_control,'Units','normalize',                ...
                       'Position',[0.875 0.02 0.125 0.96],      ...
                       'Title','Generation',            ...
                       'Tag','SignalType');   
                   
                   uicontrol('Style','pushbutton',                  ...
                             'Parent',generation_panel,             ...
                             'String','Generate',                   ...
                             'FontSize',12,                         ...
                             'FontName','Comic Sans MS',            ...
                             'Units','normalized',                  ...
                             'Position',[0.1 0.325 0.8 0.35],         ...
                             'Callback',{@generationSignalCalback,h})

            
