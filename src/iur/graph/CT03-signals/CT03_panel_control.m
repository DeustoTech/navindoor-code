                       'Tag','Tab');          
                   
                   
   color = [ 0 0 0.75];
                
   %% Beacon Based               
   tab_beacon_based_sgn   = uitab(tabgp,'Title','Beacon Based','Tag','Beacon Based');
   tab_beacon_based_sgn.ForegroundColor = color;

           popBBS = uicontrol('style','popupmenu',              ...
                              'String',{'RSS','ToF','AoA'},     ...
                              'Units','normalized',             ...
                              'Position',[0.05 0.4 0.3 0.2],    ...
                              'Parent',tab_beacon_based_sgn,    ...
                              'Tag','popupmenu');
                     
           panel_filter = uipanel(tab_beacon_based_sgn,'Title','Filter Aps','Units','normalized','Position',[0.4 0.0 0.6 1.0]);
                    
